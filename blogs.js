function renderBlogList() {
  const list = document.getElementById('blog-list');
  if (!list) return;

  const grouped = {};
  window.BLOG_POSTS.forEach(function(post) {
    if (!grouped[post.month]) grouped[post.month] = [];
    grouped[post.month].push(post);
  });

  const monthOrder = Object.keys(grouped).sort(function(a, b) {
    return new Date('1 ' + b) - new Date('1 ' + a);
  });

  var html = '';
  monthOrder.forEach(function(month, mi) {
    if (mi > 0) html += '<hr style="border:none;border-top:1px solid var(--grey-light);margin:16px 0;">';
    html += '<div class="blog-section">';
    html += '<div style="font-size:13px;font-weight:700;color:var(--grey-dark);margin-bottom:10px;text-transform:uppercase;letter-spacing:0.5px;">' + month + '</div>';
    html += '<div class="blog-grid">';
    grouped[month].forEach(function(post) {
      html += '<div class="blog-thumb-btn" onclick="openBlogPost(\'' + post.slug + '\')" style="background-image:url(\'assets/media/foromane_place_holder_image_blank.webp\');" onerror="this.style.backgroundImage=\'none\';this.style.background=\'var(--grey-light)\';this.innerHTML=\'<span style=font-size:24px;opacity:0.3;>📰</span>\';">';
      html += '<div class="blog-thumb-overlay">' + post.title + '</div>';
      html += '</div>';
    });
    html += '</div></div>';
  });

  list.innerHTML = html;
}

function openBlogPost(slug) {
  var post = window.BLOG_POSTS.find(function(p) { return p.slug === slug; });
  if (!post) return;

  var date = new Date(post.date);
  var dateStr = date.toLocaleDateString('en-BW', { weekday: 'short', day: 'numeric', month: 'short', year: 'numeric' });
  var timeStr = date.toLocaleTimeString('en-BW', { hour: '2-digit', minute: '2-digit' });

  // Build image carousel in the image section
  var imageSection = document.getElementById('blog-content-image-section');
  if (imageSection) {
    var carouselHtml = '';
    if (post.images && post.images.length > 0) {
      carouselHtml = '<div class="blog-carousel" id="blog-carousel-' + post.slug + '">';
      post.images.forEach(function(img) {
        carouselHtml += '<img src="' + img + '" class="blog-carousel-img" alt="' + post.title + '" onerror="this.parentElement.removeChild(this)">';
      });
      carouselHtml += '</div>';
      if (post.images.length > 1) {
        carouselHtml += '<div class="carousel-dots" id="bcd-' + post.slug + '">';
        post.images.forEach(function(_, i) {
          carouselHtml += '<span class="carousel-dot' + (i === 0 ? ' active' : '') + '" onclick="scrollBlogCarousel(\'' + post.slug + '\',' + i + ')"></span>';
        });
        carouselHtml += '</div>';
      }
    }
    imageSection.innerHTML = carouselHtml;
  }

  var body = document.getElementById('blog-content-body');
  if (!body) return;

  body.innerHTML =
    '<div style="padding:16px 16px 8px;">' +
      '<h1 style="font-family:var(--font-head);font-size:18px;font-weight:700;margin:0 0 6px;">' + post.title + '</h1>' +
      '<p style="font-size:12px;color:var(--grey-dark);margin:0 0 12px;">' + dateStr + ' \u00b7 ' + timeStr + '</p>' +
      '<p style="font-size:14px;line-height:1.6;margin:0;">' + post.content + '</p>' +
    '</div>' +
    '<hr style="border:none;border-top:1px solid var(--grey-light);margin:16px 0;">' +
    '<div class="blog-social-grid">' +
      '<img src="assets/icons/facebook_icon_on.png" class="blog-social-icon" onclick="openFacebook(\'Foromane\')">' +
      '<img src="assets/icons/Instagram_On.png" class="blog-social-icon" onclick="showToast(\'@foromane_bw\')">' +
      '<img src="assets/icons/LinkedIn_On.png" class="blog-social-icon" onclick="showToast(\'Foromane Construction Hub\')">' +
      '<img src="assets/icons/TikTok_On.png" class="blog-social-icon" onclick="showToast(\'@foromane_bw\')">' +
      '<img src="assets/icons/Twitter_On.png" class="blog-social-icon" onclick="showToast(\'@foromane_bw\')">' +
      '<img src="assets/icons/solid/share-nodes_whatsapp_green.webp" class="blog-social-icon" onclick="openWhatsApp(\'26772000002\',\'Foromane\')">' +
      '<img src="assets/icons/YouTube_On.png" class="blog-social-icon" onclick="showToast(\'Foromane Construction Hub\')">' +
    '</div>';

  goTo('view-blog-content');
}

function scrollBlogCarousel(slug, index) {
  var carousel = document.getElementById('blog-carousel-' + slug);
  if (!carousel) return;
  var imgs = carousel.querySelectorAll('img');
  if (imgs.length === 0) return;
  var scrollLeft = 0;
  for (var i = 0; i < index; i++) {
    scrollLeft += imgs[i].offsetWidth || 300;
  }
  carousel.scrollTo({ left: scrollLeft, behavior: 'smooth' });

  var dotsContainer = document.getElementById('bcd-' + slug);
  if (dotsContainer) {
    dotsContainer.querySelectorAll('.carousel-dot').forEach(function(d, di) {
      d.classList.toggle('active', di === index);
    });
  }
}

